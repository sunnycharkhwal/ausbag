<script>
    $(document).ready(function() {
        const searchParams = new URLSearchParams(window.location.search);
        $(".accordion-item").on("click", function() {
            let collSpanId = $(this).find(".accordion-button");
            let collSpanLable = $(this).find(`[aria-labelledby="${collSpanId.attr("id")}"]`);
            searchParams.set("category", collSpanId.attr("id"));
            const newUrl = `?${searchParams.toString()}`;
            window.history.pushState(null, "", newUrl);
        })

        $(".nav-link").on("click", function() {
            let navName = $(this).attr("id");
            searchParams.set("product-page", navName);
            const newUrl = `?${searchParams.toString()}`;
            window.history.pushState(null, "", newUrl);
        })

        const listName = searchParams.get("category")
        if (listName) {
            if ($(`#${listName}`).attr('aria-expanded') == "false") {
                $(`#${listName}`).trigger("click");
            }
        }

        const productPage = searchParams.get("product-page")
        if (productPage) {
            $(`#${productPage}`).trigger("click");
            $(`#${productPage}`).addClass("active");
        }
    })
</script>
